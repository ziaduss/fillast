#include "fillit.h"

int			ft_nbr_bloc(char *str)
{
	int		i;
	int		cont;

	i = 0;
	cont = 0;
	while (str[i])
	{
		i++;
		if (str[i] == '\n' && (str[i + 1] == '\n' || str[i + 1] == '\0'))
			cont++;
	}
	return (cont);
}
